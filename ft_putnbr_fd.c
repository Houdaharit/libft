#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int		n1;

	if (n < 0)
	{
		n1 = n * -1;
		write (1, "-", 1);
	}
	else
		n1 = n;
	if (n1)
	{
		ft_putnbr_fd (n1 / 10, fd);
		ft_putchar_fd ((n1 % 10) + '0', fd);
	}	
}
