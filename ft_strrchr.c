#include <stdlib.h>

char	*ft_strrchr(char *s, int c)
{
	char	*ptr;

	ptr = NULL;
	if (c == '\0')
	{
		while (*s)
			s ++;
		return (s);
	}
	while (*s)
	{
		if (*s == c)
			ptr = s;
		s ++;
	}
	return (ptr);
}
