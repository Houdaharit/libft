#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*temp;
	t_list	*head;

	if (!lst)
		return (NULL);
	head = ft_lstnew((*f)(lst->content));
	new = head;
	lst = lst->next;
	while (lst)
	{
		temp = ft_lstnew(f(lst->content));
		if (!temp)
		{
			ft_lstclear(&head, del);
			return (NULL);
		}
		new->next = temp;
		new = temp;
		lst = lst->next;
	}
	return (head);
}
